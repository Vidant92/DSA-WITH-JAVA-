package methods;

public class swapping {
    public static void main(String[] args) {
        int a = 4;
        int b= 5 ;
        System.out.println(" before swapping " + a + b );
        int temp = a ;
        a= b;
        b= temp;
        System.out.println(" after swapping "+a+ " "  +b );
    }
}
